import pandas as pd


def build_results_dataframe(
    X_test,
    y_test,
    y_pred,
):
    """
    Build a DataFrame containing texts, true labels, and predictions.

    Parameters
    ----------
    X_test : pandas.Series
        Test input texts or features.
    y_test : pandas.Series
        True labels associated with the test data.
    y_pred : array-like
        Predicted labels generated by the model.

    Returns
    -------
    pandas.DataFrame
        DataFrame with the following columns:
        
        - ``text``: original test samples
        - ``real``: true labels
        - ``predicted``: predicted labels
    """

    return pd.DataFrame({
        "text": X_test.reset_index(drop=True),
        "real": y_test.reset_index(drop=True),
        "predicted": y_pred,
    })


def get_correct_predictions(results_df):
    """
    Return rows where the prediction matches the true label.

    Parameters
    ----------
    results_df : pandas.DataFrame
        DataFrame containing ``real`` and ``predicted`` columns.

    Returns
    -------
    pandas.DataFrame
        Subset containing correctly classified samples.
    """

    return results_df[
        results_df["real"] == results_df["predicted"]
    ]


def get_incorrect_predictions(results_df):
    """
    Return rows where the prediction does not match the true label.

    Parameters
    ----------
    results_df : pandas.DataFrame
        DataFrame containing ``real`` and ``predicted`` columns.

    Returns
    -------
    pandas.DataFrame
        Subset containing incorrectly classified samples.
    """

    return results_df[
        results_df["real"] != results_df["predicted"]
    ]


def get_false_positives(
    results_df,
    positive_class,
):
    """
    Return false positive predictions for a given positive class.

    Parameters
    ----------
    results_df : pandas.DataFrame
        DataFrame containing ``real`` and ``predicted`` columns.
    positive_class : str or int
        Label considered as the positive class.

    Returns
    -------
    pandas.DataFrame
        Rows where the model incorrectly predicted the positive class.
    """

    return results_df[
        (results_df["real"] != positive_class)
        & (results_df["predicted"] == positive_class)
    ]


def get_false_negatives(
    results_df,
    positive_class,
):
    """
    Return false negative predictions for a given positive class.

    Parameters
    ----------
    results_df : pandas.DataFrame
        DataFrame containing ``real`` and ``predicted`` columns.
    positive_class : str or int
        Label considered as the positive class.

    Returns
    -------
    pandas.DataFrame
        Rows where the model failed to predict the positive class.
    """

    return results_df[
        (results_df["real"] == positive_class)
        & (results_df["predicted"] != positive_class)
    ]